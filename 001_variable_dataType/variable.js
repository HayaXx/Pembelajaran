/**
 * VARIABLE
 */

// VAR
// Var, bisa diakses diluar scopes, nilainya bisa diubah
var nama = "Ogata";
nama = "Rizu";
console.log("Nama:", nama); // Output: Nama: Rizu

// LET
// let, dia tidak bisa di akses diluar scopes, hanya di dalam scopes, dan nilainya bisa diubah
let umur = 17;
umur = 18;
console.log("Umur:", umur); // Output: Umur: 18

// CONST
// const, dia tidak bisa di akses diluar scopes, hanya di dalam scopes, dan nilainya tidak bisa diubah
const hobi = "Programming";
console.log("Hobi:", hobi); // Output: Hobi: Programming

